package com.dbsoft.whjd.pageModel;

public class WebServiceDataInteractionPage {
	private Integer id;
	private Integer csId;
	private Integer reportId;
	private String methodName;
	private Integer stationId;
	private Integer lineLocalId;
	private Integer status;
	private String licence;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getCsId() {
		return csId;
	}
	public void setCsId(Integer csId) {
		this.csId = csId;
	}
	public Integer getReportId() {
		return reportId;
	}
	public void setReportId(Integer reportId) {
		this.reportId = reportId;
	}
	public String getMethodName() {
		return methodName;
	}
	public void setMethodName(String methodName) {
		this.methodName = methodName;
	}
	public Integer getStationId() {
		return stationId;
	}
	public void setStationId(Integer stationId) {
		this.stationId = stationId;
	}
	public Integer getLineLocalId() {
		return lineLocalId;
	}
	public void setLineLocalId(Integer lineLocalId) {
		this.lineLocalId = lineLocalId;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public String getLicence() {
		return licence;
	}
	public void setLicence(String licence) {
		this.licence = licence;
	}
	
}
